package JavaDemo;

public class SwitchCasesDemo {
	public static void main(String[] args) {
		int Option=3;
		switch(Option) {
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
			break;
		case 3:
			System.out.println("3");
			break;
		default:
			System.out.println("Default");
		}
	}
}
